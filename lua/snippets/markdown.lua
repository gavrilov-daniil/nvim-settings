local ls = require("luasnip")
local s = ls.snippet
local t = ls.text_node
local i = ls.insert_node

ls.add_snippets("markdown", {

  -- Заголовки
  s("h1", { t("# "), i(1, "Заголовок") }),
  s("h2", { t("## "), i(1, "Заголовок") }),
  s("h3", { t("### "), i(1, "Заголовок") }),

  -- Списки
  s("ul", {
    t({
      "- пункт 1",
      "- пункт 2",
      "- пункт 3",
    }),
  }),

  s("ol", {
    t({
      "1. пункт 1",
      "2. пункт 2",
      "3. пункт 3",
    }),
  }),

  -- Чекбоксы
  s("todo", {
    t("- [ ] "), i(1, "задача"),
  }),

  -- Таблица
  s("table", {
    t({
      "| Колонка 1 | Колонка 2 |",
      "|-----------|-----------|",
      "| значение  | значение  |",
    }),
  }),

  -- Кодовый блок
  s("code", {
    t("```"),
    i(1, "bash"),
    t({ "", "" }),
    i(2, "код"),
    t({ "", "```" }),
  }),

  -- Цитата
  s("quote", {
    t("> "), i(1, "цитата"),
  }),

})
